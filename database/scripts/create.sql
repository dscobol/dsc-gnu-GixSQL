DROP TABLE IF EXISTS emptable;

CREATE TABLE emptable (
    eno integer,
    lname varchar(10),
    fname varchar(10),
    street varchar(20),
    city varchar(15),
    st varchar(2),
    zip varchar(5),
    dept varchar(4),
    payrate numeric(13,2),
    com numeric(3,2),
    miscdata varchar(128),
    dnum1 numeric(4,2),
    dnum2 numeric(4,2),
    dnum3 numeric(4,2)
);
